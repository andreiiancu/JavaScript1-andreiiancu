// Array
var myArray = [1, 2, 3, 4, 5];

console.log(myArray[0]);
// Afiseaza numarul de pe pozitia 5 a arrayului.
console.log(myArray[5]);

// Afiseaza numarul 3 din array.
console.log(myArray[2]);

// Afiseaza lungimea arrayului.
console.log(myArray.length);

// Afiseaza suma numerelor de pe pozitia 0 si 4.
console.log(myArray[0] + myArray[4]);

// Afiseaza pozitia 1.
console.log(myArray[1]);

// Afiseaza numarul de pe pozitia 2 a arrayului.
console.log(myArray[1]);

// Afiseaza numarul 4 din array.
console.log(myArray[3]);

// Afiseaza diferenta dintre lungimea a
// rrayului si valoarea din pozitia 5.
console.log(myArray.length) - myArray[4];

// Afiseaza suma numerelor de pe pozitia 1 si 3.
console.log(myArray[0] + myArray[2]);

// Afiseaza produsul numerelor de pe pozitia 0 si 5
console.log(myArray[0] * myArray[4]);

// Afiseaza diferenta numerelor de pe pozitia 3 si 4.
console.log(myArray[2] - myArray[3]);

